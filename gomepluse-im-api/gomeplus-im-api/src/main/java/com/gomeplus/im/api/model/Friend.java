package com.gomeplus.im.api.model;

import java.io.Serializable;

/**
 * 好友
 * Created by wangshikai on 2016/2/19.
 */
public class Friend implements Serializable{
    private long id;
    private long userId;
    private long friendUserId;
    private String friendNickName;
    private String firstNickNameSpell;
    private int status; //0:未通过,1:通过,2:拒绝
    private String mark;   //备注的好友名
    private String firstMarkSpell;
    private long friendGroupId;  //好友分组的id
    private long createTime;
    private long updateTime;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getUserId() {
        return userId;
    }

    public void setUserId(long userId) {
        this.userId = userId;
    }

    public long getFriendUserId() {
        return friendUserId;
    }

    public void setFriendUserId(long friendUserId) {
        this.friendUserId = friendUserId;
    }

    public String getFriendNickName() {
        return friendNickName;
    }

    public void setFriendNickName(String friendNickName) {
        this.friendNickName = friendNickName;
    }

    public String getFirstNickNameSpell() {
        return firstNickNameSpell;
    }

    public void setFirstNickNameSpell(String firstNickNameSpell) {
        this.firstNickNameSpell = firstNickNameSpell;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getMark() {
        return mark;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }

    public String getFirstMarkSpell() {
        return firstMarkSpell;
    }

    public void setFirstMarkSpell(String firstMarkSpell) {
        this.firstMarkSpell = firstMarkSpell;
    }

    public long getFriendGroupId() {
        return friendGroupId;
    }

    public void setFriendGroupId(long friendGroupId) {
        this.friendGroupId = friendGroupId;
    }

    public long getCreateTime() {
        return createTime;
    }

    public void setCreateTime(long createTime) {
        this.createTime = createTime;
    }

    public long getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(long updateTime) {
        this.updateTime = updateTime;
    }
}
