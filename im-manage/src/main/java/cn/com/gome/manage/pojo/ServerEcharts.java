package cn.com.gome.manage.pojo;

import java.util.ArrayList;
import java.util.List;

public class ServerEcharts {
	public List<String> legend = new ArrayList<String>();//数据分组
    public List<String> axis = new ArrayList<String>();//横坐标
    public List<ServerSeries> series = new ArrayList<ServerSeries>();//纵坐标
    public ServerEcharts(List<String> legendList, List<String> categoryList, List<ServerSeries> seriesList) {
        super();
        this.legend = legendList;
        this.axis = categoryList;
        this.series = seriesList;
    }

    public List<String> getLegend() {
        return legend;
    }

    public void setLegend(List<String> legend) {
        this.legend = legend;
    }

    public List<String> getAxis() {
        return axis;
    }

    public void setAxis(List<String> axis) {
        this.axis = axis;
    }

    public List<ServerSeries> getSeries() {
        return series;
    }

    public void setSeries(List<ServerSeries> series) {
        this.series = series;
    }
}
