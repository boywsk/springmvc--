package com.gomeplus.im.manage.pojo;

import java.io.Serializable;

/**
 * 群相关操作
 */
public class OperateGroup implements Serializable {
	private static final long serialVersionUID = 1L;

	private int optType;// 操作类型；1:加入群，2:退群，3:修改群，4:解散群
	private int receiverType;// 消息接收者类型；1:全部成员，2:管理员
	private JoinGroup joinGroup;// 加入群
	private QuitGroup quitGroup;// 退群
	private EditGroup editGroup;// 修改群
	private DisbandGroup disbandGroup; // 解散群
	private String extra;//扩展

	public int getOptType() {
		return optType;
	}

	public void setOptType(int optType) {
		this.optType = optType;
	}

	public int getReceiverType() {
		return receiverType;
	}

	public void setReceiverType(int receiverType) {
		this.receiverType = receiverType;
	}

	public JoinGroup getJoinGroup() {
		return joinGroup;
	}

	public void setJoinGroup(JoinGroup joinGroup) {
		this.joinGroup = joinGroup;
	}

	public QuitGroup getQuitGroup() {
		return quitGroup;
	}

	public void setQuitGroup(QuitGroup quitGroup) {
		this.quitGroup = quitGroup;
	}

	public EditGroup getEditGroup() {
		return editGroup;
	}

	public void setEditGroup(EditGroup editGroup) {
		this.editGroup = editGroup;
	}

	public DisbandGroup getDisbandGroup() {
		return disbandGroup;
	}

	public void setDisbandGroup(DisbandGroup disbandGroup) {
		this.disbandGroup = disbandGroup;
	}

	public String getExtra() {
		return extra;
	}

	public void setExtra(String extra) {
		this.extra = extra;
	}

}
