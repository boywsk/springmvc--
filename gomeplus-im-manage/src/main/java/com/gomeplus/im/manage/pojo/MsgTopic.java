package com.gomeplus.im.manage.pojo;

import java.io.Serializable;
import java.util.List;

/**
 * 话题
 */
public class MsgTopic implements Serializable {
	private static final long serialVersionUID = 1L;

	private long id; // 话题id
	private int topicType; // 话题类型；1:文本、2:商品、3:店铺、4:语音
	private String title; // 标题
	private String content; // 内容
	private long createTime; // 发送时间
	private List<MsgGoods> goods; // 商品
	private List<MsgShop> shop; // 店铺
	private List<Attachment> msgAttch; //附件列表
	private String extra;//扩展

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getTopicType() {
		return topicType;
	}

	public void setTopicType(int topicType) {
		this.topicType = topicType;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public long getCreateTime() {
		return createTime;
	}

	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}

	public List<MsgGoods> getGoods() {
		return goods;
	}

	public void setGoods(List<MsgGoods> goods) {
		this.goods = goods;
	}

	public List<MsgShop> getShop() {
		return shop;
	}

	public void setShop(List<MsgShop> shop) {
		this.shop = shop;
	}

	public List<Attachment> getMsgAttch() {
		return msgAttch;
	}

	public void setMsgAttch(List<Attachment> msgAttch) {
		this.msgAttch = msgAttch;
	}

	public String getExtra() {
		return extra;
	}

	public void setExtra(String extra) {
		this.extra = extra;
	}

}
